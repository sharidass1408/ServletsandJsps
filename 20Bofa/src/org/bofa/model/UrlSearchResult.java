package org.bofa.model;

public class UrlSearchResult {

	private String pacexecutable;
	private String pacfile;
	private String pacurl;
	
	public String getPacexecutable() {
		return pacexecutable;
	}
	public void setPacexecutable(String pacexecutable) {
		this.pacexecutable = pacexecutable;
	}
	public String getPacfile() {
		return pacfile;
	}
	public void setPacfile(String pacfile) {
		this.pacfile = pacfile;
	}
	public String getPacurl() {
		return pacurl;
	}
	public void setPacurl(String pacurl) {
		this.pacurl = pacurl;
	}

}
